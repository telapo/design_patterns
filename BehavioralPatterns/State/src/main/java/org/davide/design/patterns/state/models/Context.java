package org.davide.design.patterns.state.models;

/**
 * This is the Context.
 * 
 * It maintains an instance to the current state.
 * 
 * @author davide
 */
public class Context {

    private State state;

    public Context() {
        state = null;
    }

    public void setState(State state) {
        this.state = state;
    }

    public State getState() {
        return state;
    }
}
